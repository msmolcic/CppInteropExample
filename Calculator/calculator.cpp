#pragma once

#include "pch.h"
#include "calculator.h"

int Sum(const int first, const int second)
{
	return first + second;
}

int Diff(const int first, const int second)
{
	return first - second;
}

int Mult(const int first, const int second)
{
	return first * second;
}

int Div(const int first, const int second)
{
	return (int)(first / second);
}
